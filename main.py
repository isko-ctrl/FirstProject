x = int(input("Введите первое число:"))
y = int(input("Введите второе число:"))
c = input("Введите арифметическое действие:")
if c == "+":
    print("Результат:", x+y)
elif c == "-":
    print("Результат:", x-y)
elif c == "*":
    print("Результат:", x*y)
elif c == "/":
    print("Результат:", x/y)